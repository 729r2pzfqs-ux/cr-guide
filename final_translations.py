    for chem in chemicals:
        if chem.get('name_en'):
            continue
        
        name = chem['name']
        if name in EXTRA_TRANSLATIONS:
            chem['name_en'] = EXTRA_TRANSLATIONS[name]
            translated += 1
            print(f"✓ {name} → {EXTRA_TRANSLATIONS[name]}")
    
    with open(data_dir / "chemicals_burkle.json", 'w', encoding='utf-8') as f:
        json.dump(chemicals, f, ensure_ascii=False, indent=2)
    
    total_en = sum(1 for c in chemicals if c.get('name_en'))
    print(f"\nNewly translated: {translated}")
    print(f"Total with English: {total_en}/{len(chemicals)} ({total_en/len(chemicals)*100:.1f}%)")
    
    remaining = [c['name'] for c in chemicals if not c.get('name_en')]
    if remaining:
        print(f"\nRemaining ({len(remaining)}):")
        for n in remaining:
            print(f"  - {n}")
