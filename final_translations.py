def main():
    data_dir = Path(__file__).parent / "data"
    
    # Load chemical data
    with open(data_dir / "chemicals_burkle.json", 'r', encoding='utf-8') as f:
        chemicals = json.load(f)
    
    # Apply translations
    translated = 0
    still_need = []
    
    for chem in chemicals:
        if chem.get('name_en'):
            continue
        
        name = chem['name']
        
        # Direct match
        if name in FINAL_TRANSLATIONS:
            chem['name_en'] = FINAL_TRANSLATIONS[name]
            translated += 1
            print(f"✓ {name} → {FINAL_TRANSLATIONS[name]}")
        else:
            # Try base name variations
            base = name.split(',')[0].strip()
            if base in FINAL_TRANSLATIONS:
                chem['name_en'] = FINAL_TRANSLATIONS[base]
                translated += 1
                print(f"✓ {name} → {FINAL_TRANSLATIONS[base]}")
            else:
                still_need.append(name)
    
    # Save updated JSON
    with open(data_dir / "chemicals_burkle.json", 'w', encoding='utf-8') as f:
        json.dump(chemicals, f, ensure_ascii=False, indent=2)
    
    # Stats
    total_en = sum(1 for c in chemicals if c.get('name_en'))
    print(f"\n{'='*50}")
    print(f"Newly translated: {translated}")
    print(f"Total with English: {total_en}/{len(chemicals)} ({total_en/len(chemicals)*100:.1f}%)")
    
    if still_need:
        print(f"\nStill need ({len(still_need)}):")
        for name in still_need:
            print(f"  - {name}")

if __name__ == "__main__":
    main()

